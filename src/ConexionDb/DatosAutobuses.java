    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //Boton Modificar 
        int fila = this.tblPersona.getSelectedRow();
        if(fila== -1){
            JOptionPane.showMessageDialog(this,"Para ACTUALIZAR un registro primero debe seleccionarlo");
        }
        else{
            try {
                Connection con2=null;
                ConexionBase conectado=new ConexionBase();
                con2=conectado.getConnection();
                Statement st=con2.createStatement();
                String SQLModifica="UPDATE Autobuses1 SET linea=?,cantidadPasajeros=?,horaSalida=?,origen=?,destino=? WHERE conductor=?";
                PreparedStatement pst=con2.prepareStatement(SQLModifica);
                pst.setString(6,txtConductor.getText());
                pst.setString(1,txtLinea.getText());
                pst.setInt(2,Integer.parseInt(txtCantidad.getText()));
                pst.setString(3,txtHorario.getText());
                pst.setString(4,txtOrigen.getText());
                pst.setString(5,txtDestino.getText());
                int s=pst.executeUpdate();
                pst.close();
                
                if(s>0){
                    JOptionPane.showMessageDialog(this,"Datos Actualizados Correctamente");
                    limpiar();
                    MostrarDatos();
                    MostrarRegistroTabla();
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Error al actualizar los Datos" + e);
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Boton eliminar
        int fila=this.tblPersona.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this,"Para ELIMINAR un registro primero debes seleccionarlo");
        }
        else{
            int Eliminar=JOptionPane.showConfirmDialog(null,"¿Está seguro de que desea ELIMINAR este registro?","Pregunta",JOptionPane.ERROR_MESSAGE,JOptionPane.YES_NO_OPTION);
            if(Eliminar==JOptionPane.YES_OPTION){
                try {
                    Connection con3=null;
                    ConexionBase Conexion = new ConexionBase();
                    con3=Conexion.getConnection();
                    Statement st= con3.createStatement();
                    String SQLEliminar="DELETE * FROM Autobuses1 WHERE conductor=?";
                    PreparedStatement pst=con3.prepareStatement(SQLEliminar);
                    pst.setString(1,txtConductor.getText());
                    int n=pst.executeUpdate();
                    if(n>0){
                        JOptionPane.showMessageDialog(this,"Registro Eliminado Correctamente");
                        limpiar();
                        MostrarDatos();
                        MostrarRegistroTabla();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,"Error al eliminar los Datos"+e);
                }
            }
            
        }
         
    }//GEN-LAST:event_btnEliminarActionPerformed

