    public void MostrarDatos(){
        DefaultTableModel MiTabla =(DefaultTableModel)tblPersona.getModel();
        String EncabezadoTabla[]={"Linea","Conductor","Cantidad de Pasajeros","Horario de Salida","Origen","Destino"};
        MiTabla = new DefaultTableModel(null,EncabezadoTabla);
        tblPersona.setModel(MiTabla);
                
    }
    
    public void MostrarRegistroTabla(){
        try {
            Connection Conex = null;
            DefaultTableModel lamismatabla= (DefaultTableModel)tblPersona.getModel();
            ConexionBase conectame = new ConexionBase();
            Conex = conectame.getConnection();
            String Registro[]=new String[6];
            String SQL="SELECT * FROM Autobuses1";
            Statement sentencia=Conex.createStatement();
            ResultSet rst=sentencia.executeQuery(SQL);
            while(rst.next()){
                
                Registro[0]=rst.getString("linea");
                Registro[1]=rst.getString("conductor");
                Registro[2]=rst.getString("cantidadPasajeros");
                Registro[3]=rst.getString("horaSalida");
                Registro[4]=rst.getString("origen");
                Registro[5]=rst.getString("destino");
                lamismatabla.addRow(Registro);
            }
        tblPersona.setModel(lamismatabla);
            
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(null,"Error Tabla "+ e);
        }
    }
    
