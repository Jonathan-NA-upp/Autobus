    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        if(txtLinea.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta ingresar la Linea");
            txtLinea.requestFocus();
        }
        else{
            if(txtConductor.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta ingresar el Conductor");
            txtConductor.requestFocus();    
            }
            else{
                try {
                    Connection con1 = null;
                    ConexionBase conectado= new ConexionBase();
                    con1=conectado.getConnection();
                    Statement st = con1.createStatement();
                    String SQLGuardar ="INSERT INTO Autobuses1(linea,conductor,cantidadPasajeros,horaSalida,origen,destino)VALUES(?,?,?,?,?,?)";
                    PreparedStatement pst =con1.prepareCall(SQLGuardar);
                    pst.setString(1,txtLinea.getText());
                    pst.setString(2,txtConductor.getText());
                    pst.setInt(3,Integer.parseInt(txtCantidad.getText()));
                    pst.setString(4,txtHorario.getText());
                    pst.setString(5,txtOrigen.getText());
                    pst.setString(6,txtDestino.getText());
                    int n = pst.executeUpdate();
                    pst.close();
                  if(n>0){
                      JOptionPane.showMessageDialog(null,"Datos Guardados Correctamente");
                      limpiar();
                      MostrarDatos();
                      MostrarRegistroTabla();
                      
                  }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null,"Error al Guardar Datos"+e);
                }
            }
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

